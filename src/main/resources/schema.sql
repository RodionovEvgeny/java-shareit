
CREATE TABLE IF NOT EXISTS users (
    id long generated by default as identity PRIMARY KEY,
    name varchar(50) NOT NULL,
    email varchar   NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS items (
    id long generated by default as identity PRIMARY KEY,
    name varchar(50) NOT NULL,
    description varchar(300)   NOT NULL,
    owner long   NOT NULL,
    available boolean NOT NULL,
    request long,
    CONSTRAINT fk_items_owner FOREIGN KEY(owner) REFERENCES users (id)
);

CREATE TYPE IF NOT EXISTS booking_status AS ENUM ('WAITING', 'APPROVED', 'REJECTED');

CREATE TABLE IF NOT EXISTS bookings (
    id long generated by default as identity PRIMARY KEY,
    start_time timestamp without time zone NOT NULL,
    end_time timestamp without time zone NOT NULL,
    item long   NOT NULL,
    booker long   NOT NULL,
    status booking_status,
    CONSTRAINT fk_bookings_item FOREIGN KEY(item) REFERENCES items (id),
    CONSTRAINT fk_bookings_booker FOREIGN KEY(booker) REFERENCES users (id)
);



